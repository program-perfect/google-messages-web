function makeMsg(
  id: string,
  convId: string,
  text: string,
  direction: "incoming" | "outgoing",
  minutesAgo: number,
  status: "sent" | "delivered" | "read" = "read",
  reactions: { emoji: string; count: number }[] = []
): Message {
  return {
    id,
    conversationId: convId,
    text,
    timestamp: subMinutes(now, minutesAgo).toISOString(),
    direction,
    status,
    reactions,
    isDeleted: false,
    replyTo: null,
  };
}

